#capitalize makes the first char upper case and the rest of the chars lower case 
p "hello World".capitalize

puts

#Capitalize method ignors numbers and special chars
p "how are you @ 5  ".capitalize

#upcase makes all the chars upper case
lowercase = "rumman"
uppercase = lowercase.upcase
p uppercase

#downcase makes everything smaller 
ucase = "RUMMAN"
dcase = ucase.downcase 
p dcase 

#swap case reverses the case
p "boRIs".swapcase
